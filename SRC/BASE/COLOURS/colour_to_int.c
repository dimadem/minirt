#include "types.h"

int	colour_to_int(t_trgb data)
{
	int	r;
	int	g;
	int	b;

	r = (int)(data.r * 255);
	g = (int)(data.g * 255);
	b = (int)(data.b * 255);
	if (r < 0)
		r = 0;
	if (r > 255)
		r = 255;
	if (g < 0)
		g = 0;
	if (g > 255)
		g = 255;
	if (b < 0)
		b = 0;
	if (b > 255)
		b = 255;
	return ((r << 16) | (g << 8) | b);
}
